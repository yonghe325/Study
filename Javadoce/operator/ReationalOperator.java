//关系运算符
		//==
		//!=
		// <
		// >
		// <=
		// >=
		//instanceof,判断对象是否为类
public class ReationalOperator{
	public static void main(String[] args) {
		int a = 9;
		int b = 8;
		System.out.println(a > b );
		System.out.println(a >= b );
		System.out.println(a <= b);
		System.out.println(a < b);
		System.out.println(a == b);
		System.out.println(a != b);
		boolean flag = a > b;
    	System.out.println(flag);

	}
}
